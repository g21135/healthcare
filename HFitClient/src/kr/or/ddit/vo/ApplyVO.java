package kr.or.ddit.vo;

import java.io.Serializable;

public class ApplyVO implements Serializable{
	
	private int apply_no;
	private String apply_name;
	private int apply_bir;
	private String apply_addr;
	private String apply_tel;
	private String apply_careeri;
	private String apply_gender;
	private String apply_email;
	private int check;
	
	public ApplyVO(int apply_no, String apply_name, int apply_bir, String apply_addr, String apply_tel,
			String apply_careeri, String apply_gender, String apply_email, int check) {
		super();
		this.apply_no = apply_no;
		this.apply_name = apply_name;
		this.apply_bir = apply_bir;
		this.apply_addr = apply_addr;
		this.apply_tel = apply_tel;
		this.apply_careeri = apply_careeri;
		this.apply_gender = apply_gender;
		this.apply_email = apply_email;
		this.check = check;
	}

	public int getCheck() {
		return check;
	}
	
	public String getApply_email() {
		return apply_email;
	}


	public void setApply_email(String apply_email) {
		this.apply_email = apply_email;
	}


	public void setCheck(int check) {
		this.check = check;
	}

	public ApplyVO() {
		
	}
	
	public int getApply_no() {
		return apply_no;
	}
	public void setApply_no(int apply_no) {
		this.apply_no = apply_no;
	}
	public String getApply_name() {
		return apply_name;
	}
	public void setApply_name(String apply_name) {
		this.apply_name = apply_name;
	}
	public int getApply_bir() {
		return apply_bir;
	}
	public void setApply_bir(int apply_bir) {
		this.apply_bir = apply_bir;
	}
	public String getApply_addr() {
		return apply_addr;
	}
	public void setApply_addr(String apply_addr) {
		this.apply_addr = apply_addr;
	}
	public String getApply_tel() {
		return apply_tel;
	}
	public void setApply_tel(String apply_tel) {
		this.apply_tel = apply_tel;
	}
	public String getApply_careeri() {
		return apply_careeri;
	}
	public void setApply_careeri(String apply_careeri) {
		this.apply_careeri = apply_careeri;
	}
	public String getApply_gender() {
		return apply_gender;
	}
	public void setApply_gender(String apply_gender) {
		this.apply_gender = apply_gender;
	}
	

}
