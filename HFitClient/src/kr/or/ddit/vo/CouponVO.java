package kr.or.ddit.vo;

import java.io.Serializable;

public class CouponVO implements Serializable {

	private String coupon_no; // 쿠폰번호
	private String mem_id; // 회원 아이디
	private String coupon_name; // 쿠폰 이름
	private String coupon_priod_start; // 발급일
	private String coupon_priod_end; // 만료일자
	private String coupon_price; //할인률
	private boolean coupon_check; // 사용여부
	
	
	public CouponVO() {
		super();
	}

	public String getCoupon_no() {
		return coupon_no;
	}


	public void setCoupon_no(String coupon_no) {
		this.coupon_no = coupon_no;
	}


	public String getMem_id() {
		return mem_id;
	}


	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}


	public String getCoupon_name() {
		return coupon_name;
	}


	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}


	public String getCoupon_priod_start() {
		return coupon_priod_start;
	}


	public void setCoupon_priod_start(String coupon_priod_start) {
		this.coupon_priod_start = coupon_priod_start;
	}


	public String getCoupon_priod_end() {
		return coupon_priod_end;
	}


	public void setCoupon_priod_end(String coupon_priod_end) {
		this.coupon_priod_end = coupon_priod_end;
	}


	public String getCoupon_price() {
		return coupon_price;
	}


	public void setCoupon_price(String coupon_price) {
		this.coupon_price = coupon_price;
	}


	public boolean isCoupon_check() {
		return coupon_check;
	}


	public void setCoupon_check(boolean coupon_check) {
		this.coupon_check = coupon_check;
	}


	public CouponVO(String coupon_no, String mem_id, String coupon_name, String coupon_priod_start,
			String coupon_priod_end, String coupon_price, boolean coupon_check) {
		super();
		this.coupon_no = coupon_no;
		this.mem_id = mem_id;
		this.coupon_name = coupon_name;
		this.coupon_priod_start = coupon_priod_start;
		this.coupon_priod_end = coupon_priod_end;
		this.coupon_price = coupon_price;
		this.coupon_check = coupon_check;
	}
	
	
	
	
}
