package kr.or.ddit.vo;

import java.io.Serializable;
import java.util.Date;

public class CheckVO implements Serializable{
	private int check_no;
	private int challapply_no;
	private String check_title;
	private String check_content;
	private String check_photo;
	private Date check_date;
	private int check_approve;
	
	public CheckVO(int check_no, int challapply_no, String check_title, String check_content, String check_photo,
			Date check_date, int check_approve) {
		super();
		this.check_no = check_no;
		this.challapply_no = challapply_no;
		this.check_title = check_title;
		this.check_content = check_content;
		this.check_photo = check_photo;
		this.check_date = check_date;
		this.check_approve = check_approve;
	}
	
	public CheckVO() {
		
	}
	public int getCheck_no() {
		return check_no;
	}
	public void setCheck_no(int check_no) {
		this.check_no = check_no;
	}
	public int getChallapply_no() {
		return challapply_no;
	}
	public void setChallapply_no(int challapply_no) {
		this.challapply_no = challapply_no;
	}
	public String getCheck_title() {
		return check_title;
	}
	public void setCheck_title(String check_title) {
		this.check_title = check_title;
	}
	public String getCheck_content() {
		return check_content;
	}
	public void setCheck_content(String check_content) {
		this.check_content = check_content;
	}
	public String getCheck_photo() {
		return check_photo;
	}
	public void setCheck_photo(String check_photo) {
		this.check_photo = check_photo;
	}
	public Date getCheck_date() {
		return check_date;
	}
	public void setCheck_date(Date check_date) {
		this.check_date = check_date;
	}
	public int getCheck_approve() {
		return check_approve;
	}
	public void setCheck_approve(int check_approve) {
		this.check_approve = check_approve;
	}
	
	

}
