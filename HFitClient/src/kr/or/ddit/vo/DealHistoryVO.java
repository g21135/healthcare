package kr.or.ddit.vo;

import java.io.Serializable;

public class DealHistoryVO implements Serializable{
	private int deal_no;
	   private int cart_no;
	   private int card_number;
	   private String deal_date;
	   private String order_prodname ;
	   private String order_prodqty ;
	   private String order_prodprice ;
	   private String order_prodship ;
	   private int order_prodshipprice ;
	   private String order_img;
	   private String order_allprice ;
	   private String mem_id ;
	   private int paycheck ;
	
	
	
	public DealHistoryVO() {
		
	}



	public DealHistoryVO(int deal_no, int cart_no, int card_number, String deal_date, String order_prodname,
			String order_prodqty, String order_prodprice, String order_prodship, int order_prodshipprice,
			String order_img, String order_allprice, String mem_id, int paycheck) {
		super();
		this.deal_no = deal_no;
		this.cart_no = cart_no;
		this.card_number = card_number;
		this.deal_date = deal_date;
		this.order_prodname = order_prodname;
		this.order_prodqty = order_prodqty;
		this.order_prodprice = order_prodprice;
		this.order_prodship = order_prodship;
		this.order_prodshipprice = order_prodshipprice;
		this.order_img = order_img;
		this.order_allprice = order_allprice;
		this.mem_id = mem_id;
		this.paycheck = paycheck;
	}



	public int getDeal_no() {
		return deal_no;
	}



	public void setDeal_no(int deal_no) {
		this.deal_no = deal_no;
	}



	public int getCart_no() {
		return cart_no;
	}



	public void setCart_no(int cart_no) {
		this.cart_no = cart_no;
	}



	public int getCard_number() {
		return card_number;
	}



	public void setCard_number(int card_number) {
		this.card_number = card_number;
	}



	public String getDeal_date() {
		return deal_date;
	}



	public void setDeal_date(String deal_date) {
		this.deal_date = deal_date;
	}



	public String getOrder_prodname() {
		return order_prodname;
	}



	public void setOrder_prodname(String order_prodname) {
		this.order_prodname = order_prodname;
	}



	public String getOrder_prodqty() {
		return order_prodqty;
	}



	public void setOrder_prodqty(String order_prodqty) {
		this.order_prodqty = order_prodqty;
	}



	public String getOrder_prodprice() {
		return order_prodprice;
	}



	public void setOrder_prodprice(String order_prodprice) {
		this.order_prodprice = order_prodprice;
	}



	public String getOrder_prodship() {
		return order_prodship;
	}



	public void setOrder_prodship(String order_prodship) {
		this.order_prodship = order_prodship;
	}



	public int getOrder_prodshipprice() {
		return order_prodshipprice;
	}



	public void setOrder_prodshipprice(int order_prodshipprice) {
		this.order_prodshipprice = order_prodshipprice;
	}



	public String getOrder_img() {
		return order_img;
	}



	public void setOrder_img(String order_img) {
		this.order_img = order_img;
	}



	public String getOrder_allprice() {
		return order_allprice;
	}



	public void setOrder_allprice(String order_allprice) {
		this.order_allprice = order_allprice;
	}



	public String getMem_id() {
		return mem_id;
	}



	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}



	public int getPaycheck() {
		return paycheck;
	}



	public void setPaycheck(int paycheck) {
		this.paycheck = paycheck;
	}



	
	

}
