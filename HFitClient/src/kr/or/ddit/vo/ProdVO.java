package kr.or.ddit.vo;

import java.io.Serializable;
import java.util.Date;

public class ProdVO implements Serializable {
	
	private static final long serialVersionUID = 1L;
	private int prod_no; // 상품번호
	private int div_code; // 구분코드
	private String prod_name; // 상품이름
	private String prod_content; // 상품내용
	private String prod_photo; //상품 사진
	private int prod_price; //상품 가격
	private int remain_curstock;
	private int remain_outstock;
	private int deal_no;
	private String mem_name;
	private String deal_date;
	private String mem_tel;
	private int prod_check;
	
	
	
	public ProdVO() {
		super();
	}



	public ProdVO(int prod_no, int div_code, String prod_name, String prod_content, String prod_photo, int prod_price,
			int remain_curstock, int remain_outstock, int deal_no, String mem_name, String deal_date, String mem_tel,
			int prod_check) {
		super();
		this.prod_no = prod_no;
		this.div_code = div_code;
		this.prod_name = prod_name;
		this.prod_content = prod_content;
		this.prod_photo = prod_photo;
		this.prod_price = prod_price;
		this.remain_curstock = remain_curstock;
		this.remain_outstock = remain_outstock;
		this.deal_no = deal_no;
		this.mem_name = mem_name;
		this.deal_date = deal_date;
		this.mem_tel = mem_tel;
		this.prod_check = prod_check;
	}



	public int getProd_no() {
		return prod_no;
	}



	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}



	public int getDiv_code() {
		return div_code;
	}



	public void setDiv_code(int div_code) {
		this.div_code = div_code;
	}



	public String getProd_name() {
		return prod_name;
	}



	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}



	public String getProd_content() {
		return prod_content;
	}



	public void setProd_content(String prod_content) {
		this.prod_content = prod_content;
	}



	public String getProd_photo() {
		return prod_photo;
	}



	public void setProd_photo(String prod_photo) {
		this.prod_photo = prod_photo;
	}



	public int getProd_price() {
		return prod_price;
	}



	public void setProd_price(int prod_price) {
		this.prod_price = prod_price;
	}



	public int getRemain_curstock() {
		return remain_curstock;
	}



	public void setRemain_curstock(int remain_curstock) {
		this.remain_curstock = remain_curstock;
	}



	public int getRemain_outstock() {
		return remain_outstock;
	}



	public void setRemain_outstock(int remain_outstock) {
		this.remain_outstock = remain_outstock;
	}



	public int getDeal_no() {
		return deal_no;
	}



	public void setDeal_no(int deal_no) {
		this.deal_no = deal_no;
	}



	public String getMem_name() {
		return mem_name;
	}



	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}



	public String getDeal_date() {
		return deal_date;
	}



	public void setDeal_date(String deal_date) {
		this.deal_date = deal_date;
	}



	public String getMem_tel() {
		return mem_tel;
	}



	public void setMem_tel(String mem_tel) {
		this.mem_tel = mem_tel;
	}



	public int getProd_check() {
		return prod_check;
	}



	public void setProd_check(int prod_check) {
		this.prod_check = prod_check;
	}



}
