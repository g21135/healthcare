package kr.or.ddit.view.dealHistroy;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

public class DealHistroyMain extends Application{

	@Override
	public void start(Stage primaryStage) throws Exception {
		
//		FXMLLoader loader = new FXMLLoader(getClass().getResource("../view/wantgo/mypage_wantGo.fxml"));
		FXMLLoader loader = new FXMLLoader(getClass().getResource("DealHistroy.fxml"));
		Parent root = loader.load();
		Scene scene = new Scene(root);
		
		
		primaryStage.setTitle("DealHistroy");
		primaryStage.setScene(scene);
		primaryStage.show();
		

	}
	
	public static void main(String[] args) {
		
		launch(args);
	}

}
