package kr.or.ddit.vo;

import java.io.Serializable;

public class CartVO implements Serializable {
	
	private static final long serialVersionUID = 1L;
	private int cart_no;//장바구니번호
	private String mem_id;//회원아이디
	private int prod_no;
	private String cart_name;
	private String cart_qty;
	private String cart_content;
	private String cart_photo;
	private String cart_price;
	private String cart_order;
	private int cart_check;
	private String cart_ship;
	private int cart_shipprice;
	

	public CartVO() {
		super();
	}


	public CartVO(int cart_no, String mem_id, int prod_no, String cart_name, String cart_qty, String cart_content,
			String cart_photo, String cart_price, String cart_order, int cart_check, String cart_ship,
			int cart_shipprice) {
		super();
		this.cart_no = cart_no;
		this.mem_id = mem_id;
		this.prod_no = prod_no;
		this.cart_name = cart_name;
		this.cart_qty = cart_qty;
		this.cart_content = cart_content;
		this.cart_photo = cart_photo;
		this.cart_price = cart_price;
		this.cart_order = cart_order;
		this.cart_check = cart_check;
		this.cart_ship = cart_ship;
		this.cart_shipprice = cart_shipprice;
	}


	public int getCart_no() {
		return cart_no;
	}


	public void setCart_no(int cart_no) {
		this.cart_no = cart_no;
	}


	public String getMem_id() {
		return mem_id;
	}


	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}


	public int getProd_no() {
		return prod_no;
	}


	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}


	public String getCart_name() {
		return cart_name;
	}


	public void setCart_name(String cart_name) {
		this.cart_name = cart_name;
	}


	public String getCart_qty() {
		return cart_qty;
	}


	public void setCart_qty(String cart_qty) {
		this.cart_qty = cart_qty;
	}


	public String getCart_content() {
		return cart_content;
	}


	public void setCart_content(String cart_content) {
		this.cart_content = cart_content;
	}


	public String getCart_photo() {
		return cart_photo;
	}


	public void setCart_photo(String cart_photo) {
		this.cart_photo = cart_photo;
	}


	public String getCart_price() {
		return cart_price;
	}


	public void setCart_price(String cart_price) {
		this.cart_price = cart_price;
	}


	public String getCart_order() {
		return cart_order;
	}


	public void setCart_order(String cart_order) {
		this.cart_order = cart_order;
	}


	public int getCart_check() {
		return cart_check;
	}


	public void setCart_check(int cart_check) {
		this.cart_check = cart_check;
	}


	public String getCart_ship() {
		return cart_ship;
	}


	public void setCart_ship(String cart_ship) {
		this.cart_ship = cart_ship;
	}


	public int getCart_shipprice() {
		return cart_shipprice;
	}


	public void setCart_shipprice(int cart_shipprice) {
		this.cart_shipprice = cart_shipprice;
	}


	
	 
}